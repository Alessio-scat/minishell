#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cpy;
	size_t	len;

	len = ft_strlen(s1) + 1;
	cpy = malloc(len * sizeof(char));
	if (cpy == NULL)
		return (NULL);
	ft_memcpy(cpy, s1, len);
	return (cpy);
}
